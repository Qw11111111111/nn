import numpy as np

class optim():
    
    def __init__(self) -> None:
        pass

    def backpropagation(self) -> None:
        pass

class SGD(optim):

    def __init__(self) -> None:
        super().__init__()
    
    def backpropagation(self) -> None:
        return super().backpropagation()
    
class Adam(optim):

    def __init__(self) -> None:
        super().__init__()

    def backpropagation(self) -> None:
        return super().backpropagation()
    
class Momentum(optim):

    def __init__(self) -> None:
        super().__init__()

    def backpropagation(self) -> None:
        return super().backpropagation()
    
class GD(optim):

    def __init__(self) -> None:
        super().__init__()

    def backpropagation(self) -> None:
        return super().backpropagation()
